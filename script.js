    function showQuote(language) {
      const quoteList = language === 'english' ? englishQuotes : urduQuotes;
      const randomIndex = Math.floor(Math.random() * quoteList.length);
      document.getElementById("quoteDisplay").textContent = quoteList[randomIndex];
    }

      function copyQuote() {
      const quote = document.getElementById("quoteDisplay").textContent;
      navigator.clipboard.writeText(quote).then(() => {
        alert("Quote copied to clipboard!");
      });
    }